package com.example.projectgdsync;

public class ItemMaster {

    private String itemcode;
    private String itemname;
    private String itemcategory;
    private String itemstockqty;
    private String itemsaleqty;
    private String itemrecvqty;

    public ItemMaster() {
    }

    public String getItemcode() {
        return itemcode;
    }

    public void setItemcode(String itemcode) {
        this.itemcode = itemcode;
    }

    public String getItemname() {
        return itemname;
    }

    public void setItemname(String itemname) {
        this.itemname = itemname;
    }

    public String getItemcategory() {
        return itemcategory;
    }

    public void setItemcategory(String itemcategory) {
        this.itemcategory = itemcategory;
    }

    public String getItemstockqty() {
        return itemstockqty;
    }

    public void setItemstockqty(String itemstockqty) {
        this.itemstockqty = itemstockqty;
    }

    public String getItemsaleqty() {
        return itemsaleqty;
    }

    public void setItemsaleqty(String itemsaleqty) {
        this.itemsaleqty = itemsaleqty;
    }

    public String getItemrecvqty() {
        return itemrecvqty;
    }

    public void setItemrecvqty(String itemrecvqty) {
        this.itemrecvqty = itemrecvqty;
    }
}
